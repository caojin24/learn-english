function slugifyPhraseText(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 40);
}

function getPhraseDifficulty(text: string): "starter" | "basic" {
  const wordCount = text
    .replace(/[^a-zA-Z0-9'\s]/g, " ")
    .trim()
    .split(/\s+/)
    .filter(Boolean).length;

  return wordCount <= 3 ? "starter" : "basic";
}

